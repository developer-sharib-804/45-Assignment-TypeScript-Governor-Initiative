var alien_color = 'yellow';
if (alien_color === 'green') {
    console.log("Green color");
}
else if (alien_color === 'yellow') {
    console.log("Yellow Color");
}
else if (alien_color === 'red') {
    console.log("Red Color");
}
else {
    console.log("Invalid color.");
}
